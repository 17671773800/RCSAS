    private void coachNameBoxActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        String coachName = coachNameBox.getSelectedItem().toString();
        for(Coach coach : coachList){
            if(coachName.equals(coach.getCoachName())){
                sportCodeInput.setText(coach.getSportID());
                sportAddressInput.setText(coach.getCoachAddress());
                sportNameInput.setText(coach.getSportName());
            }
        }

    }

    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        String studentIDValueText = this.studentIDValue.getText();
        String studentNameInputText = this.studentNameValue.getText();
        String coachNameInputText = this.coachNameBox.getSelectedItem().toString();
        String sportCodeInputText = this.sportCodeInput.getText();
        String sportNameInputText = this.sportNameInput.getText();
        String sportAddressInputText = this.sportAddressInput.getText();
        String scheduleDateInputText = this.scheduleDateInput.getText();

        StringBuilder stringBuilder = new StringBuilder();
        stringBuilder.append(studentIDValueText);
        stringBuilder.append("\t");
        stringBuilder.append(studentNameInputText);
        stringBuilder.append("\t");
        stringBuilder.append(coachNameInputText);
        stringBuilder.append("\t");
        stringBuilder.append(sportCodeInputText);
        stringBuilder.append("\t");
        stringBuilder.append(sportNameInputText);
        stringBuilder.append("\t");
        stringBuilder.append(scheduleDateInputText);
        stringBuilder.append("\t");
        stringBuilder.append(sportAddressInputText);

        UserController.addSportSchedule(stringBuilder.toString());
        new Alert().start("Add Sport Schedule success!");
        RegisteredSportScheduleFrame.resetTableData();
    }//GEN-LAST:event_submitButtonActionPerformed

