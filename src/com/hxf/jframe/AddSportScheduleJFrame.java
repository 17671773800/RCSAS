    public void start() {
        Toolkit toolkit = Toolkit.getDefaultToolkit();
        int x = (int)(toolkit.getScreenSize().getWidth()-this.getWidth())/2;
        int y = (int)(toolkit.getScreenSize().getHeight()-this.getHeight())/2;
        this.setVisible(true);
        this.setLocation(x, y);

        // get studentID
        this.studentIDValue.setText(RegisteredStudentMenuJFrame.getStudentInfo().getId());
        // get studentName
        this.studentNameValue.setText(RegisteredStudentMenuJFrame.getStudentInfo().getName());

        sportCodeInput.setEnabled(false);
        sportAddressInput.setEnabled(false);
        sportNameInput.setEnabled(false);

        coachList = UserController.getDetailsOfCoach();
        for(Coach coach : coachList){
            coachNameBox.addItem(coach.getCoachName());
        }
    }

